const getAllCategoriesData = async () => {
  try {
    const res = await fetch(`${process.env.API_URL}/product/categories/`);
    if (!res.ok) {
      throw new Error("Failed to fetch data");
    }
    const data = await res.json();
    return data.results;
  } catch (error) {
    console.error("Error fetching data:", error);
    return [];
  }
};

